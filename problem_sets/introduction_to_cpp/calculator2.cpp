#include<iostream>
using namespace std;

int main(){
    long long a;
    long long b;
    cin >> a;

    cin >> b;
    cout << a << " + " << b << " = " << a + b <<endl;
    cout << "\n";
    cout << a << " - " << b << " = " << a - b <<endl;
    cout << "\n";
    cout << a << " * " << b << " = " << a * b <<endl;
    cout << "\n";
    cout << a << " / " << b << " = " << a / b <<endl;
    cout << "\n";
    cout << a << " % " << b << " = " << a % b;
}